const RTL: LangCode[] = ["ar"];
const STORAGE_KEY = "valore.lang";
const DEFAULT: LangCode = "pt-BR";

const translations: Record<LangCode, Dict> = LANGUAGES.reduce(
  (acc, l) => {
    acc[l.code] = Object.fromEntries(Object.entries(DICT).map(([k, v]) => [k, v[l.code]]));
    return acc;
  },
  {} as Record<LangCode, Dict>,
);

type Ctx = {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  dir: "ltr" | "rtl";
};

const LangCtx = createContext<Ctx | null>(null);

function interpolate(s: string, params?: Record<string, string | number>) {
  if (!params) return s;
  let out = s;
  for (const [k, v] of Object.entries(params)) out = out.replaceAll(`{${k}}`, String(v));
  return out;
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>(DEFAULT);

  useEffect(() => {
    try {
      const stored = localStorage.getItem(STORAGE_KEY) as LangCode | null;
      if (stored && translations[stored]) setLangState(stored);
    } catch {}
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = RTL.includes(lang) ? "rtl" : "ltr";
    }
  }, [lang]);

  const setLang = (l: LangCode) => {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {}
  };

  const t = (key: string, params?: Record<string, string | number>) =>
    interpolate(translations[lang]?.[key] ?? translations[DEFAULT][key] ?? key, params);

  return (
    <LangCtx.Provider value={{ lang, setLang, t, dir: RTL.includes(lang) ? "rtl" : "ltr" }}>
      {children}
    </LangCtx.Provider>
  );
}

export function useT() {
  const ctx = useContext(LangCtx);
  if (!ctx) {
    // SSR / outside provider fallback
    return {
      lang: DEFAULT,
      setLang: () => {},
      t: (k: string, params?: Record<string, string | number>) => interpolate(translations[DEFAULT][k] ?? k, params),
      dir: "ltr" as const,
    };
  }
  return ctx;
}

